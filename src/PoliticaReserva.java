public class PoliticaReserva {
    private String idPolitica;
    private String tipo;
    private String descripcion;
    private double valorAsociado;


    //getters y setters
    public String getIdPolitica() {
        return idPolitica;
    }
    public void setIdPolitica(String idPolitica) {
        this.idPolitica = idPolitica;
    }
    public String getTipo() {
        return tipo;
    }
    public void setTipo(String tipo) {
        this.tipo = tipo;
    }
    public String getDescripcion() {
        return descripcion;
    }
    public void setDescripcion(String descripcion) {
        this.descripcion = descripcion;
    }
    public double getValorAsociado() {
        return valorAsociado;
    }
    public void setValorAsociado(double valorAsociado) {
        this.valorAsociado = valorAsociado;
    }

    
}
